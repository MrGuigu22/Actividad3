package Capitulo3Ej19;

public class Calculos {
    
    public static double CalcularP(double a){
        return 3 * a;
    }
    
    public static double CalcularAL(double a){
        return Math.sqrt(3)* (a/2);
    }
    
    public static double CalcularAR(double a){
        return ((Math.sqrt(3)* (a/2))*a)/2;
    }
    
}
